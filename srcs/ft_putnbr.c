#include "libft.h"

void	ft_putnbr(int n)
{
	int		i;
	char	num[10];

	if (n == 0)
	{
		write(1, "0", 1);
		return ;
	}
	i = 0;
	if (n < 0)
	{
		num[i++] = (n % 10) * (-1) + '0';
		n = (n / 10) * (-1);
		write(1, "-", 1);
	}
	while (n)
	{
		num[i++] = n % 10 + '0';
		n = n / 10;
	}
	i--;
	while (i >= 0)
		write(1, &num[i--], 1);
}
